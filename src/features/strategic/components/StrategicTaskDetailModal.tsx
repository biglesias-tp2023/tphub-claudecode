import {
  X,
  Calendar,
  User,
  Target,
  Clock,
  CheckCircle2,
  Circle,
  Pencil,
  Trash2,
  Building2,
  Users,
  Briefcase,
} from 'lucide-react';
import { Modal, Badge, Button } from '@/components/ui';
import { cn } from '@/utils/cn';
import { getCategoryConfig } from '../config';
import { AvatarInitials } from './AvatarInitials';
import type { StrategicTaskWithDetails, ObjectiveResponsible } from '@/types';

interface StrategicTaskDetailModalProps {
  isOpen: boolean;
  onClose: () => void;
  task: StrategicTaskWithDetails | null;
  onEdit: () => void;
  onDelete: () => void;
  onToggleComplete: () => void;
  isDeleting?: boolean;
}

const RESPONSIBLE_LABELS: Record<ObjectiveResponsible, { label: string; icon: React.ElementType }> = {
  thinkpaladar: { label: 'ThinkPaladar', icon: Building2 },
  cliente: { label: 'Cliente', icon: User },
  ambos: { label: 'Ambos', icon: Users },
  plataforma: { label: 'Plataforma', icon: Briefcase },
};

/**
 * Format deadline for display
 */
function formatDeadline(deadline: string | null): string {
  if (!deadline) return 'Sin fecha';

  const date = new Date(deadline);
  const options: Intl.DateTimeFormatOptions = {
    weekday: 'long',
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  };

  const time = date.getHours() !== 0 || date.getMinutes() !== 0
    ? ` a las ${date.getHours().toString().padStart(2, '0')}:${date.getMinutes().toString().padStart(2, '0')}`
    : '';

  return date.toLocaleDateString('es-ES', options) + time;
}

/**
 * Calculate days remaining
 */
function getDaysRemaining(deadline: string | null): { days: number; label: string; color: string } | null {
  if (!deadline) return null;

  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const deadlineDate = new Date(deadline);
  deadlineDate.setHours(0, 0, 0, 0);

  const diffTime = deadlineDate.getTime() - today.getTime();
  const days = Math.ceil(diffTime / (1000 * 60 * 60 * 24));

  if (days < 0) {
    return { days: Math.abs(days), label: `${Math.abs(days)} días de retraso`, color: 'text-red-600' };
  }
  if (days === 0) {
    return { days: 0, label: 'Vence hoy', color: 'text-orange-600' };
  }
  if (days <= 3) {
    return { days, label: `Vence en ${days} días`, color: 'text-orange-600' };
  }
  if (days <= 7) {
    return { days, label: `Vence en ${days} días`, color: 'text-yellow-600' };
  }
  return { days, label: `Vence en ${days} días`, color: 'text-gray-600' };
}

/**
 * Modal showing full details of a strategic task
 */
export function StrategicTaskDetailModal({
  isOpen,
  onClose,
  task,
  onEdit,
  onDelete,
  onToggleComplete,
  isDeleting,
}: StrategicTaskDetailModalProps) {
  if (!task) return null;

  const categoryConfig = getCategoryConfig(task.category);
  const responsibleInfo = RESPONSIBLE_LABELS[task.responsible];
  const ResponsibleIcon = responsibleInfo?.icon || User;
  const daysRemaining = getDaysRemaining(task.deadline);

  // Determine display name and avatar variant
  const avatarVariant = task.responsible === 'cliente' ? 'client' : 'primary';
  const displayName = task.responsible === 'cliente'
    ? task.clientName || 'Cliente'
    : task.assigneeName || 'Sin asignar';

  return (
    <Modal isOpen={isOpen} onClose={onClose} size="md">
      <div className="p-6">
        {/* Header */}
        <div className="flex items-start justify-between mb-4">
          <div className="flex-1">
            {/* Category badge */}
            {categoryConfig && (
              <div className="mb-2">
                <Badge className={cn(categoryConfig.bgColor, categoryConfig.textColor)}>
                  {categoryConfig.label}
                </Badge>
              </div>
            )}

            {/* Title */}
            <h2 className={cn(
              'text-xl font-semibold text-gray-900',
              task.isCompleted && 'line-through text-gray-500'
            )}>
              {task.title}
            </h2>
          </div>

          <button
            onClick={onClose}
            className="p-2 text-gray-400 hover:text-gray-600 rounded-lg hover:bg-gray-100 transition-colors"
          >
            <X className="w-5 h-5" />
          </button>
        </div>

        {/* Status badge */}
        <div className="mb-4">
          {task.isCompleted ? (
            <Badge variant="success" className="inline-flex items-center gap-1">
              <CheckCircle2 className="w-3 h-3" />
              Completada
            </Badge>
          ) : (
            <Badge variant="secondary" className="inline-flex items-center gap-1">
              <Circle className="w-3 h-3" />
              Pendiente
            </Badge>
          )}
          {task.isAutoGenerated && (
            <Badge variant="secondary" className="ml-2">
              Auto-generada
            </Badge>
          )}
        </div>

        {/* Description */}
        {task.description && (
          <div className="mb-6">
            <h3 className="text-sm font-medium text-gray-700 mb-1">Descripción</h3>
            <p className="text-sm text-gray-600 whitespace-pre-wrap">{task.description}</p>
          </div>
        )}

        {/* Meta info */}
        <div className="space-y-3 mb-6">
          {/* Deadline */}
          <div className="flex items-center gap-3">
            <Calendar className="w-4 h-4 text-gray-400" />
            <div>
              <span className="text-sm text-gray-600">{formatDeadline(task.deadline)}</span>
              {daysRemaining && !task.isCompleted && (
                <span className={cn('ml-2 text-sm font-medium', daysRemaining.color)}>
                  ({daysRemaining.label})
                </span>
              )}
            </div>
          </div>

          {/* Responsible */}
          <div className="flex items-center gap-3">
            <ResponsibleIcon className="w-4 h-4 text-gray-400" />
            <span className="text-sm text-gray-600">
              {responsibleInfo?.label || task.responsible}
            </span>
          </div>

          {/* Assignee */}
          <div className="flex items-center gap-3">
            <AvatarInitials
              name={displayName}
              avatarUrl={task.assigneeAvatarUrl}
              size="sm"
              variant={avatarVariant}
            />
            <span className="text-sm text-gray-600">{displayName}</span>
          </div>

          {/* Linked objective */}
          {task.objectiveTitle && (
            <div className="flex items-start gap-3">
              <Target className="w-4 h-4 text-gray-400 mt-0.5" />
              <div>
                <span className="text-xs text-gray-500">Objetivo vinculado</span>
                <p className="text-sm text-gray-700">{task.objectiveTitle}</p>
              </div>
            </div>
          )}

          {/* Completed at */}
          {task.isCompleted && task.completedAt && (
            <div className="flex items-center gap-3">
              <Clock className="w-4 h-4 text-gray-400" />
              <span className="text-sm text-gray-600">
                Completada el {new Date(task.completedAt).toLocaleDateString('es-ES', {
                  day: 'numeric',
                  month: 'long',
                  year: 'numeric',
                })}
              </span>
            </div>
          )}
        </div>

        {/* Actions */}
        <div className="flex items-center justify-between pt-4 border-t border-gray-100">
          <div className="flex items-center gap-2">
            <Button
              variant="outline"
              size="sm"
              onClick={onEdit}
              className="gap-1.5"
            >
              <Pencil className="w-4 h-4" />
              Editar
            </Button>
            <Button
              variant="outline"
              size="sm"
              onClick={onDelete}
              disabled={isDeleting}
              className="gap-1.5 text-red-600 hover:text-red-700 hover:bg-red-50"
            >
              <Trash2 className="w-4 h-4" />
              {isDeleting ? 'Eliminando...' : 'Eliminar'}
            </Button>
          </div>

          <Button
            variant={task.isCompleted ? 'outline' : 'primary'}
            size="sm"
            onClick={onToggleComplete}
            className="gap-1.5"
          >
            {task.isCompleted ? (
              <>
                <Circle className="w-4 h-4" />
                Marcar pendiente
              </>
            ) : (
              <>
                <CheckCircle2 className="w-4 h-4" />
                Completar
              </>
            )}
          </Button>
        </div>
      </div>
    </Modal>
  );
}
